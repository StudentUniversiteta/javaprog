package ru.mirea.lab9_Sort_Comparator.n3;

public class Students {
    private int iDNumber;
    private String name;

    public Students(int iDNumber, String name) {
        this.iDNumber = iDNumber;
        this.name = name;
    }

    public int getIDNumber() {
        return iDNumber;
    }

    public String getName() {
        return name;
    }
}
